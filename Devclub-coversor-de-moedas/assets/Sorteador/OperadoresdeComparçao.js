/*


Operadores de Comparação
== -> Igual // Compara o valor,mas não o tipo
=== -> Idêntico // Compara o valor e o tipo

!= -> Diferente // Compara o valor,mas não o tipo
!== -> Não idêntico // Compara o valor e o tipo


*/

const firstNumber = 30
const secondNumber = "30"

console.log(firstNumber != secondNumber) // true







if(firstNumber != secondNumber){
    console.log("Os valores são diferentes")
}else {
    console.log("Não, Os valores não são diferentes")
}