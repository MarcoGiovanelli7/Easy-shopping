/*
 Switch Case -Controlor de Fluxo
*/
const user = 'Giovanna'

switch (user) {
    case "Marco":
        console.log('eu')
        break

    case "Roberta":
        console.log('minha esposa')
        break

    case "Giovanna":
        console.log('tá quente minha filha')
        break

    
    default:
        console.log('Não sei te dizer')

}